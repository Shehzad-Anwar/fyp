export async function getServerSideProps(context) {
  if (!mongoose.connection.readyState) {
    await mongoose.connect(process.env.MONGO_URI);
  }
  const { slug, Category } = context.query;
  console.log(slug, Category);
  let products = await Product.find({ category: slug }).sort({ _id: -1 });
  // let products = await Product.find({'availableQty': {$ne : 0}, category:"T-shirt"})
  let tshirts = {};
  // Loop though all products {T-shirt},
  // if item in tshirt object, update its color and size array.
  //To findout which are avilable in what verients.
  for (let item of products) {
    if (item.title in tshirts) {
      if (
        !tshirts[item.title].color.includes(item.color) &&
        item.availableQty > 0
      ) {
        tshirts[item.title].color.push(item.color);
      }
      if (
        !tshirts[item.title].size.includes(item.size) &&
        item.availableQty > 0
      ) {
        tshirts[item.title].size.push(item.size);
      }
    } else {
      tshirts[item.title] = JSON.parse(JSON.stringify(item));
      if (item.availableQty > 0) {
        tshirts[item.title].color = [item.color];
        tshirts[item.title].size = [item.size];
      } else {
        tshirts[item.title].color = [];
        tshirts[item.title].size = [];
      }
    }
  }
  return {
    props: { products: JSON.parse(JSON.stringify(tshirts)), slug }, // will be passed to the page component as props
  };
}

